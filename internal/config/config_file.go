// CurrentConfigVersion is bumped when config schema changes require migration or wizard re-run.
const CurrentConfigVersion = "0.8.0"

const configSchemaRawBaseURL = "https://raw.githubusercontent.com/pinchtab/pinchtab"

// ConfigSchemaURL is the source-checkout/default JSON Schema URL. Released
// binaries replace this with their own release tag at command startup.
const ConfigSchemaURL = configSchemaRawBaseURL + "/main/schema/config.json"

var currentConfigSchemaURL = ConfigSchemaURL

// publishedConfigSchemaVersions lists release tags known to contain
// schema/config.json. Keep this empty until the first schema-bearing release is
// published; unknown versions fall back to main rather than broken old tags.
var publishedConfigSchemaVersions []string

// SetConfigSchemaVersion switches generated configs to the schema hosted on
// the release tag for a concrete release build. Non-release builds keep main,
// which avoids pointing at old config-version tags that predate the schema.
func SetConfigSchemaVersion(version string) {
	currentConfigSchemaURL = schemaURLForVersion(version)
}

// CurrentConfigSchemaURL returns the schema URL generated by this process.
func CurrentConfigSchemaURL() string {
	return currentConfigSchemaURL
}

func schemaURLForVersion(version string) string {
	version = strings.TrimPrefix(strings.TrimSpace(version), "v")
	if !isReleaseVersion(version) {
		return ConfigSchemaURL
	}
	schemaVersion, ok := closestPublishedConfigSchemaVersion(version)
	if !ok {
		return ConfigSchemaURL
	}
	return configSchemaRawBaseURL + "/v" + schemaVersion + "/schema/config.json"
}

func isReleaseVersion(version string) bool {
	_, ok := parseSemver(version)
	return ok
}

func closestPublishedConfigSchemaVersion(version string) (string, bool) {
	target, ok := parseSemver(version)
	if !ok {
		return "", false
	}

	// Use an older-or-equal schema so generated configs never point at rules
	// from a future PinchTab release.
	closest := ""
	for _, candidate := range publishedConfigSchemaVersions {
		parsed, ok := parseSemver(candidate)
		if !ok || compareSemver(parsed, target) > 0 {
			continue
		}
		if closest == "" {
			closest = candidate
			continue
		}
		closestParsed, _ := parseSemver(closest)
		if compareSemver(parsed, closestParsed) > 0 {
			closest = candidate
		}
	}
	return closest, closest != ""
}

func parseSemver(version string) ([3]int, bool) {
	var out [3]int
	parts := strings.Split(strings.TrimPrefix(strings.TrimSpace(version), "v"), ".")
	if len(parts) != 3 {
		return out, false
	}
	for i, part := range parts {
		if part == "" {
			return out, false
		}
		n, err := strconv.Atoi(part)
		if err != nil {
			return out, false
		}
		out[i] = n
	}
	return out, true
}

func compareSemver(a, b [3]int) int {
	for i := range a {
		if a[i] < b[i] {
			return -1
		}
		if a[i] > b[i] {
			return 1
		}
	}
	return 0
}

